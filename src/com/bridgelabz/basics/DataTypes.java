package com.bridgelabz.basics;

public class DataTypes {

    // Variable => It is a place which has a memory assigned to it and it can store some data
    //
    // primitive and non-primitive data types
    // primitive => byte,short,int,long,float,double,char,boolean
    // Non-primitive =>

    // javatpoint(concepts) , geekforgeeks(examples)
    public static void main(String[] args) {

        byte b = 10;  //0  // 1 byte  //  -128 to 127
        short s = 20; //0  // 2 byte  //
        int x = 10;   //0  // 4 byte
        long l = 24;  //0  // 8 byte
        float f = 10.0f; //0.0F  // 4 byte
        double d = 10.76; //0.0  // 8 byte
        char c = 'a';    // \u00000  // 2 byte
        boolean flag = true; //false // 1 bit

        System.out.println(b);
        System.out.println(s);
        System.out.println(x);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(flag);

    }
}
